    private void TabelproductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelproductMouseClicked
        // TODO add your handling code here:
        int row = Tabelproduct.getSelectedRow();
        
        if (row != -1){
            String id = Tabelproduct.getValueAt(row, 0).toString();
            String name = Tabelproduct.getValueAt(row, 1).toString();
            String category = Tabelproduct.getValueAt(row, 2).toString();
            String description = Tabelproduct.getValueAt(row, 3).toString();
            String price = Tabelproduct.getValueAt(row, 4).toString();
            
            Tid.setText(id);
            Tname.setText(name);
            Tdescription.setText(description);
            Tprice.setText(price);
            
            if (category.equals("1")|| category.equalsIgnoreCase("Active")){
                Ccategory.setSelectedItem("Active");
            }else{
                Ccategory.setSelectedItem("Inactive");
            }
        }
    }//GEN-LAST:event_TabelproductMouseClicked

