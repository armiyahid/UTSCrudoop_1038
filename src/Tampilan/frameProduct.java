      void autoID(){
        Product catID = new Product();
        ResultSet rsVar = catID.autoId();
        try {
            
            if (rsVar.next()) {
                int id = rsVar.getInt("ID") + 1;
                Tid.setText(String.valueOf(id));
            } else {
                Tid.setText("1");
            }
            
        } catch (SQLException sQLException) {
            JOptionPane.showMessageDialog(null, "Error : " + sQLException.getMessage());
        }
    }    
    
    void reset(){
        autoID();
        Tname.setText(null);
        Tid.setEditable(false);
        Tdescription.setText(null);
        Tprice.setText(null);
        Ccategory.setSelectedItem(null);
    }
    void Load_table(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Username");
        model.addColumn("Category");
        model.addColumn("Deskripsi");
        model.addColumn("Price");
        
        try {
            Product prd = new Product();
            ResultSet result = prd.TampilProduct();
            while (result.next()){
                
                model.addRow(new Object[]{
                    result.getInt("ProductID"),
                    result.getString("ProductName"),
                    result.getString("categoryName"),
                    result.getString("ProductDescription"),
                    result.getInt("ProductPrice"),
                    
                });
            }
            Tabelproduct.setModel(model);
        } catch (SQLException e) {
            System.out.println("Eror : " + e.getMessage());
        }
        
    }
    void valueComboBox(){
        
        try {
            Category value = new Category();
            ResultSet rs = value.dataComboBox();

            while (rs.next()) {
                String data = rs.getString("categoryName");
                Ccategory.addItem(data);
            }

        } catch (SQLException sQLException) {
            JOptionPane.showMessageDialog(null, "Error : " + sQLException.getMessage());
        }
    
    }
    
